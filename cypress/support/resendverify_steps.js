Cypress.Commands.add('ResendVerificationBuyer', (email) => {
  cy.get('.forgot-pass-link.hidden-xs').contains('Resend email verification').click()
  cy.get('input[placeholder="Your Email"]')
    .type(email)
    .should('have.value', email)
  cy.get('.btn.btn-basic').contains('Send').click()
});
